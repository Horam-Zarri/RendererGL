class Cube {
public:

    Cube() {
        VBLayout layout;

        layout.push<float>(3);

        m_VBO.send_data(cubeVertices, sizeof cubeVertices);
        m_VAO.send_data(m_VBO, layout);
    }

    void Draw() {
        m_VAO.bind();
        glDrawArrays(GL_TRIANGLES, 0, 36);
        m_VAO.unbind();
    }
private:
    VAO m_VAO;
    VBO m_VBO;

};

